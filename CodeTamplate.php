  <h3>Explanations</h3>
  <p>This is a best practice that benefits all users. There are a number of ways in which screen readers that support scannability allow users to skim pages to get an overall impression of a page’s content. One common way to skim a page with a screen reader is to jump from heading to heading. While skimming with a screen reader, users can hear an overview of the page’s key information, then backtrack to read the parts they are most interested in. Unfortunately, too many websites lack headings on pages. Without headings, this method of skimming through content is virtually impossible. Bearing this in mind, 
    it is a crucial rule that designers organize content with headings that, to the extent possible, represent an accurate overview of the content on the page.</p>
