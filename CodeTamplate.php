  <h3>Explanations</h3>
  <p>Test colors to make sure they have the proper contrast ratio for readability, 
    using tools such as the Stark plugin for Sketch. Tools such as this one test combinations of text color on background color, and also take text size into consideration. For example, WCAG 2.0 level AA requires a contrast ratio of 4.5:1 for normal text and 3:1 for large text (i.e., 14 point and bold or larger, or 18 point or larger). Color contrast checkers typically check for compliance with this standard, 
    so it might be a good idea to bookmark the standards and guidelines WCAG has laid out.</p>

    <strong> The contrast ratio of the black text on the golden yellow background passes with a 10.35:1. The white text on the golden yellow background fails however, with a contrast ratio of 2.03:1.</strong> <p></p>

